"""
Generate a comparison report from benchmark results.

Usage:
    python report.py
"""

import json
from pathlib import Path

from tabulate import tabulate

RESULTS_DIR = Path(__file__).parent / "results"
REPORT_FILE = RESULTS_DIR / "COMPARISON.md"

MODEL_ORDER = [
    "gemini_embedding_2",
    "marengo_3",
    "internvideo2_6b",
    "xclip_base",
    "nova_multimodal",
    "siglip2_so400m",
]


def load_results() -> dict:
    """Load all individual model result files."""
    results = {}
    for f in RESULTS_DIR.glob("*.json"):
        if f.name == "all_models.json":
            continue
        with open(f) as fh:
            data = json.load(fh)
            model_name = data.get("model_info", {}).get("name", f.stem)
            results[model_name] = data
    return results


def fmt(val: float | None, decimals: int = 3) -> str:
    if val is None:
        return "—"
    return f"{val:.{decimals}f}"


def generate_report(results: dict) -> str:
    """Generate a markdown comparison report."""
    lines = []
    lines.append("# Video Embedding Benchmark — Results\n")
    lines.append(f"Models evaluated: {len(results)}\n")

    # Main comparison table
    headers = [
        "Model",
        "Dims",
        "NDCG@5",
        "NDCG@10",
        "R@1",
        "R@5",
        "MRR",
        "mAP@10",
        "Latency (ms)",
        "Vec Size",
    ]

    rows = []
    for model_name in MODEL_ORDER:
        if model_name not in results:
            continue
        r = results[model_name]
        info = r["model_info"]
        agg = r["aggregated"]
        lat = r["latency"]["video_embed_ms"]

        rows.append([
            info["display_name"],
            info["dimensions"],
            fmt(agg.get("ndcg@5", {}).get("mean")),
            fmt(agg.get("ndcg@10", {}).get("mean")),
            fmt(agg.get("recall@1", {}).get("mean")),
            fmt(agg.get("recall@5", {}).get("mean")),
            fmt(agg.get("mrr", {}).get("mean")),
            fmt(agg.get("map@10", {}).get("mean")),
            f"{lat['median']:.0f}",
            f"{info['vector_bytes']:,} B",
        ])

    # Sort by NDCG@10 descending (best model first)
    rows.sort(
        key=lambda r: float(r[3]) if r[3] != "—" else 0,
        reverse=True,
    )

    lines.append("## Overall Retrieval Performance\n")
    lines.append(tabulate(rows, headers=headers, tablefmt="github"))
    lines.append("")

    # Exact match breakdown
    lines.append("\n## Exact Match Queries (text → video)\n")
    exact_headers = ["Model", "Exact R@1", "Exact R@5"]
    exact_rows = []
    for model_name in MODEL_ORDER:
        if model_name not in results:
            continue
        r = results[model_name]
        info = r["model_info"]
        agg = r["aggregated"]
        by_type = r.get("by_query_type", {}).get("exact", {})
        exact_rows.append([
            info["display_name"],
            fmt(by_type.get("recall@1", {}).get("mean")),
            fmt(by_type.get("recall@5", {}).get("mean")),
        ])
    lines.append(tabulate(exact_rows, headers=exact_headers, tablefmt="github"))
    lines.append("")

    # Latency comparison
    lines.append("\n## Latency & Efficiency\n")
    lat_headers = ["Model", "Video Embed (median)", "Video Embed (p95)", "Text Embed (median)", "Type"]
    lat_rows = []
    for model_name in MODEL_ORDER:
        if model_name not in results:
            continue
        r = results[model_name]
        info = r["model_info"]
        vlat = r["latency"]["video_embed_ms"]
        tlat = r["latency"]["text_embed_ms"]
        lat_rows.append([
            info["display_name"],
            f"{vlat['median']:.0f} ms",
            f"{vlat['p95']:.0f} ms",
            f"{tlat['median']:.0f} ms",
            "API" if info["is_api"] else "Local",
        ])
    lines.append(tabulate(lat_rows, headers=lat_headers, tablefmt="github"))
    lines.append("")

    # Per-query failures (negative queries that ranked in top 5)
    lines.append("\n## Hard Negative Analysis\n")
    lines.append("Queries designed to NOT match a target video. Lower rank = worse (model confused).\n")
    for model_name in MODEL_ORDER:
        if model_name not in results:
            continue
        r = results[model_name]
        info = r["model_info"]
        negatives = [q for q in r["per_query"] if q.get("type") == "hard_negative"]
        if negatives:
            bad = [q for q in negatives if q["target_video"] in q.get("top_5_videos", [])]
            lines.append(
                f"- **{info['display_name']}**: {len(bad)}/{len(negatives)} "
                f"negatives ranked in top 5 (lower is better)"
            )

    lines.append("\n\n---\n")
    lines.append("*Generated by `report.py`. Re-run `python benchmark.py --all && python report.py` to update.*\n")

    return "\n".join(lines)


def main():
    results = load_results()
    if not results:
        print("No results found. Run benchmark.py first.")
        return

    report = generate_report(results)

    REPORT_FILE.parent.mkdir(parents=True, exist_ok=True)
    with open(REPORT_FILE, "w") as f:
        f.write(report)

    print(report)
    print(f"\nReport saved to {REPORT_FILE}")


if __name__ == "__main__":
    main()
